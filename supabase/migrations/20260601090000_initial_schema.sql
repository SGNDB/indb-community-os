create or replace function public.sync_idea_votes_count()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    update public.ideas
    set votes_count = votes_count + 1
    where id = new.idea_id;
    return new;
  elsif tg_op = 'DELETE' then
    update public.ideas
    set votes_count = greatest(votes_count - 1, 0)
    where id = old.idea_id;
    return old;
  end if;
  return null;
end;
$$;

drop trigger if exists idea_votes_count_trigger on public.idea_votes;
create trigger idea_votes_count_trigger
after insert or delete on public.idea_votes
for each row execute function public.sync_idea_votes_count();

create or replace function public.handle_new_user_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, username, full_name, role)
  values (
    new.id,
    coalesce(new.raw_user_meta_data ->> 'username', split_part(new.email, '@', 1)),
    coalesce(new.raw_user_meta_data ->> 'username', split_part(new.email, '@', 1)),
    'member'
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created_profile on auth.users;
create trigger on_auth_user_created_profile
after insert on auth.users
for each row execute function public.handle_new_user_profile();

create or replace function public.is_admin(uid uuid)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.profiles
    where id = uid
      and role in ('moderator', 'admin')
  );
$$;

